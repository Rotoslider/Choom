import type { Metadata, Viewport } from 'next';
import { Inter } from 'next/font/google';
import './globals.css';
import { Providers } from './providers';

const inter = Inter({ subsets: ['latin'] });

export const metadata: Metadata = {
  title: 'Choom AI Companion',
  description: 'A self-evolving AI companion with persistent memory',
};

// Without this, mobile browsers render the app at desktop width (zoomed-out and
// tiny). `device-width` makes it lay out at the phone's real width so the
// responsive classes kick in. Zoom is left enabled for accessibility.
export const viewport: Viewport = {
  width: 'device-width',
  initialScale: 1,
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html lang="en" suppressHydrationWarning>
      <body className={`${inter.className} antialiased`}>
        <Providers>{children}</Providers>
      </body>
    </html>
  );
}
